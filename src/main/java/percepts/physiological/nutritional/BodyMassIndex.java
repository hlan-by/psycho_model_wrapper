package percepts.physiological.nutritional;

public class BodyMassIndex {
    // Parameter for BMI
    private double bmi;

    // Constructor
    public BodyMassIndex(double bmi) {
        this.bmi = bmi;
    }

    // Getter for BMI
    public double getBMI() {
        return bmi;
    }

    // Setter for BMI
    public void setBMI(double bmi) {
        this.bmi = bmi;
    }
}
