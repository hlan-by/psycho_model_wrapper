package percepts.physiological.electrolyte_mineral;

public class Calcium {
    // Parameter for calcium level (mmol/L)
    private double calciumLevel;

    // Constructor
    public Calcium(double calciumLevel) {
        this.calciumLevel = calciumLevel;
    }

    // Getter for calcium level
    public double getCalciumLevel() {
        return calciumLevel;
    }

    // Setter for calcium level
    public void setCalciumLevel(double calciumLevel) {
        this.calciumLevel = calciumLevel;
    }
}

