package percepts.physiological.musculoskeletal;

public class BoneDensity {
    // Parameter for bone density
    private double density;

    // Constructor
    public BoneDensity(double density) {
        this.density = density;
    }

    // Getter for bone density
    public double getDensity() {
        return density;
    }

    // Setter for bone density
    public void setDensity(double density) {
        this.density = density;
    }
}