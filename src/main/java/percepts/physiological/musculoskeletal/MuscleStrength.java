package percepts.physiological.musculoskeletal;

public class MuscleStrength {
    // Parameter for muscle strength
    private double strength;

    // Constructor
    public MuscleStrength(double strength) {
        this.strength = strength;
    }

    // Getter for muscle strength
    public double getStrength() {
        return strength;
    }

    // Setter for muscle strength
    public void setStrength(double strength) {
        this.strength = strength;
    }
}