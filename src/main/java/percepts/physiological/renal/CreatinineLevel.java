package percepts.physiological.renal;

public class CreatinineLevel {
    // Parameter for creatinine level (mg/dL)
    private double creatinineLevel;

    // Constructor
    public CreatinineLevel(double creatinineLevel) {
        this.creatinineLevel = creatinineLevel;
    }

    // Getter for creatinine level
    public double getCreatinineLevel() {
        return creatinineLevel;
    }

    // Setter for creatinine level
    public void setCreatinineLevel(double creatinineLevel) {
        this.creatinineLevel = creatinineLevel;
    }
}

